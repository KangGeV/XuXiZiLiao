# ====================== 1. 字符串基础定义 ======================
s = "Hello Python 123"
print("原始字符串:", s)

# 字符串不可变：不能直接改某个字符（会报错）
# s[0] = "h"  # 取消注释会报错！字符串不能直接修改


# ====================== 2. 索引访问（取单个字符） ======================
print("\n--- 索引练习 ---")
print("第1个字符:", s[0])      # 正向索引 0 开头
print("最后1个字符:", s[-1])   # 反向索引 -1 开头
print("第7个字符:", s[6])


# ====================== 3. 切片操作（取一段字符） ======================
print("\n--- 切片练习 ---")
# 格式：s[起始:结束:步长]
print("前5个字符:", s[0:5])        # 0到5（不包含5）
print("从Python开始:", s[6:])      # 从6到结尾
print("跳过一个字符取:", s[::2])   # 步长2
print("字符串反转:", s[::-1])      # 步长-1 = 反转


# ====================== 4. 常用字符串方法 ======================
print("\n--- 常用方法练习 ---")

# 查找子串位置
print("find('Python'):", s.find('Python'))

# 统计次数
print("count('o'):", s.count('o'))

# 大小写转换
print("转大写:", s.upper())
print("转小写:", s.lower())

# 分割字符串 → 变成列表
print("按空格分割:", s.split(' '))

# 去除两端空白
s2 = "  Hello World  "
print("strip() 去空格:", s2.strip())

# 替换字符
print("替换H为C:", s.replace('H', 'C'))

# 判断开头/结尾
print("是否以Hello开头:", s.startswith('Hello'))
print("是否以123结尾:", s.endswith('123'))


# ====================== 5. 字符串遍历（可迭代） ======================
print("\n--- 遍历字符串每个字符 ---")
for char in s:
    print(char, end=" | ")